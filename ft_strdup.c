#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*ptr;
	int		i;

	ptr = malloc(ft_strlen(src) + 1);
	if (!ptr)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
